class Persona1 { // Clase persona

    String nombre; // Declarar variables
    String apellido; // Declarar variables
    int edad; // Declarar variables

    Persona1() { // Constructor vacio
    }

    Persona1(String nombre, String apellido, int edad) { // Constructor con parametros

        // Asignar el valor de las variables a los parametros
        this.nombre = nombre;
        this.apellido = apellido;
        this.edad = edad;

    }

}